CharacterPartSpriteFrame* CharacterPartSpriteFrame::create()
{
	CharacterPartSpriteFrame* pFrame = new (std::nothrow) CharacterPartSpriteFrame();
	Texture2D* pTexture = new Texture2D;

	// 그냥 데이터 긁어옴
	// auto img = new Image();
	// img->initWithImageFile("dummy.png");
	// tex->initWithImage(img);

	pTexture->initWithData(Transparent1x1Texture_v, sizeof(Transparent1x1Texture_v), Texture2D::PixelFormat::RGB888, 1, 1, {1, 1});
	pTexture->autorelease();

	SpriteFrameTexture* tex = new SpriteFrameTexture(pTexture, { 0, 0, 1, 1, 500, 500 } , -1, true);
	pFrame->initWithFrameTexture(tex);
	return pFrame;
}

