const NpkSpriteRect& CharacterPartSpriteFrame::getRect()
{
	FrameTexture* texture = getFrameTexture();
	DebugAssertMessage(texture != nullptr, "텍스쳐가 없어요.");
	return texture->getRect();
}

bool CharacterPartSpriteFrame::initWithFrameTexture(const FrameTexturePtr& frameTexture) {
	m_spFrameTexture = frameTexture;
	FrameTexture* pFrameTexture = m_spFrameTexture.GetPtr();
	Texture2D* pTexture = pFrameTexture->getTexture();
	DebugAssertMessage(pTexture, "텍스쳐가 없어요.");
	return initWithTexture(pTexture, { 0, 0, pFrameTexture->getWidthF(), pFrameTexture->getHeightF() });
}
